// コマンドの呼び出しと種類の設定
//==================================================
void CCommand::SetCommamnd(COMMANDTYPE type)
{
	// 種類の設定
	m_type = type;

	if (m_type == COMMANDTYPE_JUG)
	{
		for (int nCnt = 0; nCnt < NUM_BOTTON; nCnt++)
		{
			m_anCommandList[nCnt] = rand() % CONTROL_MAX;
		}
	}
}


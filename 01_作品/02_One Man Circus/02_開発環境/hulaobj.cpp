{

}

//***************************************************
// ジャグリングボール生成
//***************************************************
void CHula::Create(D3DXVECTOR3 pos, D3DXVECTOR3 rot)
{
	CHula* pHula = new CHula;
	pHula->Init(pos, rot);
}
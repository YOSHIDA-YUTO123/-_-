#include "hitstop.h"
#include "hitstop_controller.h"

//===================================================
// コンストラクタ
//===================================================
CHitStop::CHitStop() :
	m_pController(nullptr)
{
}

//===================================================
// デストラクタ
//===================================================
CHitStop::~CHitStop()
{
}

//===================================================
// 初期化処理
//===================================================
HRESULT CHitStop::Init(void)
{
	if (m_pController == nullptr)
	{
		m_pController = std::make_unique<CHitStopController>();
	}

	return S_OK;
}

//===================================================
// 終了処理
//===================================================
void CHitStop::Uninit(void)
{
}

//===================================================
// 更新処理
//===================================================
void CHitStop::Update(void)
{
	if (m_pController != nullptr)
	{
		// 更新処理
		m_pController->Update();
	}
}


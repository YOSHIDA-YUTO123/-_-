CStateMachine::CStateMachine()
{
}

//================================================
// デストラクタ
//================================================
CStateMachine::~CStateMachine()
{
}

//================================================
// 状態の変更
//================================================
void CStateMachine::Change(std::shared_ptr<CStateBase> pNewState)
{
	// 今の状態を生成
	if (m_pNowState != nullptr)
	{
		m_pNowState->Uninit();
		m_pNowState.reset();
		m_pNowState = nullptr;
	}

	// 状態の設定
	m_pNowState = pNewState;

	m_pNowState->Init();
}

//================================================
// 終了処理
//================================================
void CStateMachine::Uninit(void)
{
	// 破棄
	if (m_pNowState != nullptr)
	{
		m_pNowState->Uninit();
		m_pNowState.reset();
	}
}

//================================================
// 状態の更新処理
//================================================
void CStateMachine::Update(void)
{
	// 更新処理
	if (m_pNowState != nullptr)
	{
		m_pNowState->Update();
	}
}